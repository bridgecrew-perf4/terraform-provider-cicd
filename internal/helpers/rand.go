package helpers

import (
	"math/rand"
)

var letters = []rune("01234567890abcdefghijkmnoprstuvwxyz")

func NewRandSeq(n int) string {
	b := make([]rune, n)
	for i := range b {
		b[i] = letters[rand.Intn(len(letters))]
	}
	return string(b)
}
